int vesa_init(framebuffer_t *fb) {
    g_fb = fb;

    uint64_t pml4 = pml4_get_current();
    size_t fb_size = fb->pitch * fb->height;
    fb_size = (fb_size + 0x1FFFFF) & ~0x1FFFFF;

    return pml4_map_phys(pml4, fb->addr, fb->addr, fb_size, 1);
}

/* ♥ Flush turbinado! Usa rep movsb (mais rápido que loop de 32-bit) */
static inline void fast_memcpy32(uint32_t *dst, const uint32_t *src, size_t n) {
    __asm__ volatile(
        "rep movsq"
        : "+D"(dst), "+S"(src), "+c"(n)
        : 
        : "memory"
    );
}

void vesa_flush(void) {
    if (!g_fb || !g_backbuffer) return;
    uint32_t *fb = (uint32_t *)(uintptr_t)g_fb->addr;
    size_t pixels = (size_t)g_fb->pitch * g_fb->height / 4;
    fast_memcpy32(fb, g_backbuffer, pixels);
}

void vesa_draw_pixel(int x, int y, uint32_t color) {
    if (!g_fb) return;
    if (x < 0 || y < 0 || (uint32_t)x >= g_fb->width || (uint32_t)y >= g_fb->height)
        return;

    uint32_t *buf = fb_target();
    uint32_t stride = g_fb->pitch / 4;
    buf[y * stride + x] = color;
}

void vesa_fill_screen(uint32_t color) {
    if (!g_fb) return;

    uint32_t *buf = fb_target();
    uint32_t stride = g_fb->pitch / 4;

    for (uint32_t y = 0; y < g_fb->height; y++) {
        uint32_t *row = &buf[y * stride];
        for (uint32_t x = 0; x < g_fb->width; x += 8) {
            row[x] = color; row[x+1] = color; row[x+2] = color; row[x+3] = color;
            row[x+4] = color; row[x+5] = color; row[x+6] = color; row[x+7] = color;
        }
    }
}

void vesa_draw_rect(int x, int y, int w, int h, uint32_t color) {
    if (!g_fb) return;
    if (w <= 0 || h <= 0) return;

    uint32_t *buf = fb_target();
    uint32_t stride = g_fb->pitch / 4;

    int x0 = x < 0 ? 0 : x;
    int y0 = y < 0 ? 0 : y;
    int x1 = x + w;
    int y1 = y + h;

    if (x1 > (int)g_fb->width)  x1 = g_fb->width;
    if (y1 > (int)g_fb->height) y1 = g_fb->height;

    for (int py = y0; py < y1; py++) {
        for (int px = x0; px < x1; px++) {
            buf[py * stride + px] = color;
        }
    }
}

void vesa_draw_char(int x, int y, char c, uint32_t color) {
    if (!g_fb) return;
    if (c < 32 || c > 126) return;

    const uint8_t *glyph = font8x8[(unsigned char)c - 32];

    for (int row = 0; row < 8; row++) {
        uint8_t bits = glyph[row];
        for (int col = 0; col < 8; col++) {
            if (bits & (1 << col)) {
                int px = x + col;
                int py = y + row * 2;
                vesa_draw_pixel(px, py, color);
                vesa_draw_pixel(px, py + 1, color);
            }
        }
    }
}

void vesa_draw_cell(int x, int y, char c, uint32_t fg, uint32_t bg) {
    if (!g_fb) return;
    uint32_t cell[16][8];
    for (int r = 0; r < 16; r++)
        for (int cl = 0; cl < 8; cl++)
            cell[r][cl] = bg;
    if (c >= 32 && c <= 126) {
        const uint8_t *glyph = font8x8[(unsigned char)c - 32];
        for (int r = 0; r < 8; r++) {
            uint8_t bits = glyph[r];
            for (int cl = 0; cl < 8; cl++)
                if (bits & (1 << cl)) {
                    cell[r*2][cl] = fg;
                    cell[r*2+1][cl] = fg;
                }
        }
    }
    uint32_t *buf = fb_target();
    uint32_t stride = g_fb->pitch / 4;
    for (int r = 0; r < 16; r++) {
        uint32_t *src = cell[r];
        uint32_t *dst = &buf[(y + r) * stride + x];
        dst[0] = src[0]; dst[1] = src[1]; dst[2] = src[2]; dst[3] = src[3];
        dst[4] = src[4]; dst[5] = src[5]; dst[6] = src[6]; dst[7] = src[7];
    }
}

void vesa_draw_text(int x, int y, const char *text, uint32_t color) {
    int cx = x;
    int cy = y;

    while (*text) {
        char c = *text;

        if (c == '\n') {
            cx = x;
            cy += 16;
        } else {
            vesa_draw_char(cx, cy, c, color);
            cx += 8;
        }

        text++;
    }
}
